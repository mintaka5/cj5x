package org.cj5x.crypto.exception;

public class ProofException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8363531508599945381L;

	public ProofException() {
		super();
	}

	public ProofException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ProofException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProofException(String message) {
		super(message);
	}

	public ProofException(Throwable cause) {
		super(cause);
	}

	
}
